public enum EnumHttpMethod {
	POST  ("POST"	),
	GET	  ("GET"	),
	PATCH ("PATCH" 	),
	DELETE("DELETE"	);
	private final String text;    
	EnumHttpMethod(final String text) {
	      this.text = text;
	}    
	@Override
	public String toString() {
		return text;
	}
}

